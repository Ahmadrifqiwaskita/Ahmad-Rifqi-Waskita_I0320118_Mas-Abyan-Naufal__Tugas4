# Operator Bitwise
# Bitwise OR
a = 4
b = 11

c = a|b
print('nilai:',c,', binary :',format(c,'08b'))

# Bitwise SHIFT RIGHT
c = a >> b
print('nilai:',c,', binary :',format(c,'08b'))

# Bitwise XOR
c = a ^ b
print('nilai:',c,', binary :',format(c,'08b'))

# Bitwise NOT
c = ~4
print('nilai:',c,', binary :',format(c,'08b'))

# Bitwise AND
c = b & a
print('nilai:',c,', binary :',format(c,'08b'))


